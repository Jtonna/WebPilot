function cleanupExpiredPairings() {
  const db = dbModule.getDb();
  const now = Date.now();
  const cutoff = now - PAIRING_HARD_TTL_MS;
  const nowIso = new Date(now).toISOString();

  // Pending past expiresAt → 'expired'
  const expireRes = db
    .prepare(
      "UPDATE pairings SET state = 'expired', decided_at = ? WHERE state = 'pending' AND expires_at < ?"
    )
    .run(nowIso, now);

  // Hard-drop: anything created longer ago than hard TTL.
  // requested_at is ISO 8601; SQLite's julianday() comparison works on ISO 8601
  // strings, but the simplest cross-platform path is to compute the cutoff ISO
  // here in JS.
  const cutoffIso = new Date(cutoff).toISOString();
  const dropRes = db
    .prepare('DELETE FROM pairings WHERE requested_at < ?')
    .run(cutoffIso);

  const remaining = db.prepare('SELECT COUNT(*) AS c FROM pairings').get().c;
  if (expireRes.changes || dropRes.changes) {
    console.log(
      `[pairing:cleanup] expired=${expireRes.changes} dropped=${dropRes.changes} kept=${remaining}`
    );
  }
  return { expired: expireRes.changes, dropped: dropRes.changes, kept: remaining };
}

function cleanupOldPairings(maxAgeDays = 7) {
  const db = dbModule.getDb();
  const cutoff = Date.now() - maxAgeDays * 24 * 60 * 60 * 1000;
  const cutoffIso = new Date(cutoff).toISOString();
  // Drop denied/expired rows older than the cutoff. Approved/pending are
  // preserved — same touch-matrix as the JSON-backed implementation.
  const res = db
    .prepare(
      `DELETE FROM pairings
         WHERE state IN ('denied','expired')
           AND COALESCE(decided_at, requested_at) < ?`
    )
    .run(cutoffIso);
  const remaining = db.prepare('SELECT COUNT(*) AS c FROM pairings').get().c;
  if (res.changes > 0) {
    console.log(
      `[pairing:cleanupOldPairings] removed=${res.changes} kept=${remaining} ` +
        `(maxAgeDays=${maxAgeDays}, terminal-only: denied + expired)`
    );
  }
  return { removed: res.changes, kept: remaining };
}

function cleanupUnusedKeys(maxAgeMs = UNUSED_KEY_EXPIRY_MS) {
  const db = dbModule.getDb();
  const cutoff = Date.now() - maxAgeMs;
  const cutoffIso = new Date(cutoff).toISOString();
  // Active rows that have never been touched and were created before the cutoff.
  const stale = db
    .prepare(
      `SELECT id, name, api_key_hash, created_at FROM agents
         WHERE state = 'active' AND last_seen_at IS NULL AND created_at < ?`
    )
    .all(cutoffIso);
  if (stale.length === 0) return 0;
  const revokeStmt = db.prepare("UPDATE agents SET state = 'revoked' WHERE id = ?");
  for (const row of stale) {
    const keyDisplay = (row.api_key_hash || '').slice(0, 8) + '...';
    console.log(
      `[paired-keys:cleanupUnusedKeys] revoked unused key ${keyDisplay} ` +
        `for agent "${row.name}" (created ${row.created_at}, never accessed)`
    );
    revokeStmt.run(row.id);
  }
  return stale.length;
}

